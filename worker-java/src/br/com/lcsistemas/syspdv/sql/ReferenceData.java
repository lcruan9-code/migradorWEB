    // Cidades carregadas do CSV no classpath
    private static volatile List<String[]> CIDADES_LIST = null;

    private static List<String[]> loadCidades() {
        if (CIDADES_LIST != null) return CIDADES_LIST;
        synchronized (ReferenceData.class) {
            if (CIDADES_LIST != null) return CIDADES_LIST;
            List<String[]> list = new ArrayList<>(6000);
            try {
                InputStream is = ReferenceData.class.getResourceAsStream("cidades.csv");
                if (is == null) {
                    System.err.println("[ReferenceData] AVISO: cidades.csv nao encontrado no classpath.");
                    return list;
                }
                try (BufferedReader br = new BufferedReader(new InputStreamReader(is, StandardCharsets.UTF_8))) {
                    String line;
                    while ((line = br.readLine()) != null) {
                        try {
                            int c1 = line.indexOf(',');
                            int c2 = line.indexOf(',', c1+1);
                            int c3 = line.indexOf(',', c2+1);
                            if (c1 < 0 || c2 < 0 || c3 < 0) continue;
                            list.add(new String[]{
                                line.substring(0, c1),
                                line.substring(c1+1, c2),
                                line.substring(c2+1, c3),
                                line.substring(c3+1)
                            });
                        } catch (Exception e) {
                            // Ignora linha malformada
                        }
                    }
                }
            } catch (Exception e) {
                System.err.println("[ReferenceData] Erro lendo cidades.csv: " + e.getMessage());
            }
            CIDADES_LIST = list;
        }
        return CIDADES_LIST;
    }

